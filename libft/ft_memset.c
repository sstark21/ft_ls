#include "libft.h"

void	*ft_memset(void *dest, int c, size_t n)
{
	size_t	i;
	char	*j;

	j = dest;
	i = 0;
	while (i < n)
	{
		j[i] = c;
		i++;
	}
	return (dest);
}
